package repository;
public class Helper {
	public static void assertNull(HTTP hTTP) {
		assert(hTTP == null);
	}
	
	public static void assertNotNull(HTTP hTTP) {
		assert(hTTP != null);
	}
	public static void assertNull(IMediaStore iMediaStore) {
		assert(iMediaStore == null);
	}
	
	public static void assertNotNull(IMediaStore iMediaStore) {
		assert(iMediaStore != null);
	}
	public static void assertNull(Sound sound) {
		assert(sound == null);
	}
	
	public static void assertNotNull(Sound sound) {
		assert(sound != null);
	}
	public static void assertNull(AudioDB audioDB) {
		assert(audioDB == null);
	}
	
	public static void assertNotNull(AudioDB audioDB) {
		assert(audioDB != null);
	}
	
}
